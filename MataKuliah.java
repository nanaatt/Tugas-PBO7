    private void btnUPLOADActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUPLOADActionPerformed
        // TODO add your handling code here:
        JFileChooser jfc = new JFileChooser(FileSystemView.getFileSystemView().getHomeDirectory());
        int returnValue = jfc.showOpenDialog(null);
        if (returnValue == JFileChooser.APPROVE_OPTION) {
            File pilihanfile = jfc.getSelectedFile();
            System.out.println("File yang dipilih : " + pilihanfile.getAbsolutePath());

            try (BufferedReader br = new BufferedReader(new FileReader(pilihanfile))) {
                Class.forName(driver);
                conn = DriverManager.getConnection(koneksi, user, password);
                String row;
                String pemisah = ",";
                boolean berhasil = false;
                while ((row = br.readLine()) != null) {

                    String[] dataMk = row.split(pemisah);
                    if (dataMk.length == 4) {
                        String kode_mk = dataMk[0];
                        String sksStr = dataMk[1];
                        String nama_mk = dataMk[2];
                        String smtAjarStr = dataMk[3];

                        try {
                            int sks = Integer.parseInt(sksStr);
                            int smt_ajar = Integer.parseInt(smtAjarStr);

                            if (!kode_mk.isEmpty() && !sksStr.isEmpty() && !nama_mk.isEmpty() && !smtAjarStr.isEmpty()) {

                                String sql = "INSERT INTO MataKuliah (kode_mk, sks, nama_mk, smt_ajar) VALUES (?, ?, ?, ?)";
                                pstmt = conn.prepareStatement(sql);
                                pstmt.setString(1, kode_mk);
                                pstmt.setInt(2, sks);
                                pstmt.setString(3, nama_mk);
                                pstmt.setInt(4, smt_ajar);

                                pstmt.executeUpdate();
                                berhasil = true;
                            }
                        } catch (NumberFormatException e) {
                            JOptionPane.showMessageDialog(null, "Data error pada baris: " + row);
                        } catch (SQLException e) {
                            JOptionPane.showMessageDialog(null, "Tidak dapat memasukkan data ke database: " + e.getMessage());
                        }
                    } else {
                        JOptionPane.showMessageDialog(null, "Jumlah kolom tidak sesuai pada baris: " + row);
                    }
                }

                if (berhasil) {
                    JOptionPane.showMessageDialog(null, "Data berhasil di upload.");
                    tampil();
                } else {
                    JOptionPane.showMessageDialog(null, "Data gagal di upload.");
                }

            } catch (FileNotFoundException ex) {

            } catch (IOException ex) {

            } catch (ClassNotFoundException | SQLException ex) {

            } finally {
                try {
                    if (pstmt != null) {
                        pstmt.close();
                    }
                    if (conn != null) {
                        conn.close();
                    }
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(null, "Kesalahan saat menutup koneksi: " + ex.getMessage());
                }
            }
        }
    }//GEN-LAST:event_btnUPLOADActionPerformed

